// Program to Multiply two Floating Point Numbers

function multiplyTwoNumber(a, b) {
    return a * b;
}

// No need to declare them as float or double as you would in other languages like C++ or Java.
// Everything is handled under the hood by JavaScript as a Number.

let firstNumber = 2.23;
let secondNumber = 4.54;

let multiply = multiplyTwoNumber(firstNumber, secondNumber)

console.log(multiply)